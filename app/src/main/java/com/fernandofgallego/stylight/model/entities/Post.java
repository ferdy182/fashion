package com.fernandofgallego.stylight.model.entities;

public class Post {

    private int id;
    private String title;
    private String link;
    private String teaserImage;

    public int getId() {
        return id;
    }

    public String getTitle() {
        return title;
    }

    public String getLink() {
        return link;
    }

    public String getTeaserImage() {
        return teaserImage;
    }
}
